# number : Can be integer, a floating point
# number or a complex number

# # Python code to illustrate
# # abs() built-in function

# # floating point number
float_number = -54.26
float_number_absolute = abs(float_number)
print(f'Absolute Value of Floating Point number:{float_number_absolute}')

# An integer
int_number = -96
int_number_absolute = abs(int_number)
print(f'Absolute Value of integer number:{int_number_absolute}')

# A complex number
complex_number = (3 - 4j)
complex_number_absolute = abs(complex_number)
print(f'Absolute Value of complex number:{complex_number_absolute}')

